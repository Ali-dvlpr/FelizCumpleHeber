let currentActiveId = null;
const modalOverlay = document.getElementById('modalOverlay');
const modalContent = document.getElementById('modalContent');
const body = document.body;

function openModal(id) {
    const data = messagesData.find(item => item.id === id);
    if (!data) return;
    currentActiveId = id;

    document.getElementById('modalSender').textContent = data.sender;
    document.getElementById('modalTitle').textContent = data.title;
    document.getElementById('modalMessage').textContent = data.message;
    document.getElementById('modalImage').src = data.image;

    updateScrollIndicator();

    const titleElement = document.getElementById('modalTitle');
    titleElement.className = `text-3xl md:text-4xl font-display font-bold text-white uppercase tracking-wide border-l-4 border-${data.accentColor} pl-4`;

    modalOverlay.classList.remove('hidden');
    body.style.overflow = 'hidden'; 
    
    void modalOverlay.offsetWidth; // Reflow
    
    modalOverlay.classList.remove('opacity-0');
    modalOverlay.classList.add('fade-in');
    
    setTimeout(() => {
        modalContent.classList.remove('scale-95');
        modalContent.classList.add('scale-100');
    }, 50);
}

function closeModal() {
    modalContent.classList.remove('scale-100');
    modalContent.classList.add('scale-95');
    
    modalOverlay.classList.remove('fade-in');
    modalOverlay.classList.add('opacity-0');
    
    setTimeout(() => {
        modalOverlay.classList.add('hidden');
        body.style.overflow = 'auto';
        currentActiveId = null;
    }, 300);
}

function navigateModal(direction) {
    if (currentActiveId === null) return;
    const currentIndex = messagesData.findIndex(item => item.id === currentActiveId);
    if (currentIndex === -1) return;
    
    let nextIndex = currentIndex + direction;
    if (nextIndex >= messagesData.length) nextIndex = 0;
    if (nextIndex < 0) nextIndex = messagesData.length - 1;

    // Transición de salida/entrada del contenido
    modalContent.classList.remove('scale-100');
    modalContent.classList.add('scale-95');
    
    setTimeout(() => {
        const data = messagesData[nextIndex];
        currentActiveId = data.id;
        
        document.getElementById('modalSender').textContent = data.sender;
        document.getElementById('modalTitle').textContent = data.title;
        document.getElementById('modalMessage').textContent = data.message;
        document.getElementById('modalImage').src = data.image;

        updateScrollIndicator();

        const titleElement = document.getElementById('modalTitle');
        titleElement.className = `text-3xl md:text-4xl font-display font-bold text-white uppercase tracking-wide border-l-4 border-${data.accentColor} pl-4`;

        modalContent.classList.remove('scale-95');
        modalContent.classList.add('scale-100');
    }, 150);
}

modalOverlay.addEventListener('click', function(e) {
    if (e.target === modalOverlay) closeModal();
});

document.addEventListener('keydown', function(e) {
    if (modalOverlay.classList.contains('hidden')) return;

    if (e.key === 'Escape') {
        closeModal();
    } else if (e.key === 'ArrowRight' || e.key === 'Right') {
        navigateModal(1);
    } else if (e.key === 'ArrowLeft' || e.key === 'Left') {
        navigateModal(-1);
    }
});

let currentSlideIndex = 1;
let isSliderTransitioning = false;
let bannerTimer = null;

// Lógica de deslizado táctil (Swipe) para móviles
function addSwipeListener(element, onSwipeLeft, onSwipeRight) {
    let startX = 0;
    let startY = 0;
    
    element.addEventListener('touchstart', e => {
        startX = e.changedTouches[0].screenX;
        startY = e.changedTouches[0].screenY;
    }, { passive: true });
    
    element.addEventListener('touchend', e => {
        let endX = e.changedTouches[0].screenX;
        let endY = e.changedTouches[0].screenY;
        let diffX = endX - startX;
        let diffY = endY - startY;
        
        // Ignorar si el movimiento fue principalmente vertical (scroll)
        if (Math.abs(diffY) > Math.abs(diffX)) return;

        const threshold = 50;
        if (diffX < -threshold) {
            onSwipeLeft();
        } else if (diffX > threshold) {
            onSwipeRight();
        }
    }, { passive: true });
}

function scrollBanner(direction) {
    if (isSliderTransitioning) return;
    const container = document.getElementById('banner-slider');
    if (!container) return;

    // Resetear temporizador de 5 segundos
    initBannerTimer();

    isSliderTransitioning = true;
    currentSlideIndex += direction;
    
    container.style.transition = 'transform 0.5s ease-in-out';
    container.style.transform = `translateX(${-currentSlideIndex * 100}%)`;
}

function initBannerTimer() {
    if (bannerTimer) clearInterval(bannerTimer);
    bannerTimer = setInterval(() => {
        scrollBanner(1);
    }, 5000);
}

// Inicializar temporizador
initBannerTimer();

// Inicializar el escuchador de transiciones del slider
const bannerSlider = document.getElementById('banner-slider');
if (bannerSlider) {
    bannerSlider.addEventListener('transitionend', () => {
        isSliderTransitioning = false;
        
        // Salto instantáneo del clon al real
        if (currentSlideIndex === 6) {
            bannerSlider.style.transition = 'none';
            currentSlideIndex = 1;
            bannerSlider.style.transform = `translateX(${-currentSlideIndex * 100}%)`;
        } else if (currentSlideIndex === 0) {
            bannerSlider.style.transition = 'none';
            currentSlideIndex = 5;
            bannerSlider.style.transform = `translateX(${-currentSlideIndex * 100}%)`;
        }
    });

    // Activar deslizado táctil en el carrusel
    addSwipeListener(bannerSlider.parentElement, () => scrollBanner(1), () => scrollBanner(-1));
}

// Activar deslizado táctil en el modal de cartas
const modalOverlayElement = document.getElementById('modalOverlay');
if (modalOverlayElement) {
    addSwipeListener(modalOverlayElement, () => navigateModal(1), () => navigateModal(-1));
}

function updateScrollIndicator() {
    const container = document.getElementById('modalMessageScrollable');
    const indicator = document.getElementById('scrollIndicator');
    if (!container || !indicator) return;

    // Resetear posición de scroll al inicio
    container.scrollTop = 0;

    // Verificar si el texto desborda el contenedor (necesita scroll)
    setTimeout(() => {
        const isScrollable = container.scrollHeight > container.clientHeight + 10;
        if (isScrollable) {
            indicator.classList.remove('opacity-0');
            indicator.classList.add('opacity-100');
        } else {
            indicator.classList.remove('opacity-100');
            indicator.classList.add('opacity-0');
        }
    }, 200);
}

function handleModalScroll(element) {
    const indicator = document.getElementById('scrollIndicator');
    if (!indicator) return;
    
    // Ocultar si se scrollea hasta abajo (con umbral de 20px)
    const isAtBottom = element.scrollHeight - element.scrollTop <= element.clientHeight + 20;
    if (isAtBottom) {
        indicator.classList.remove('opacity-100');
        indicator.classList.add('opacity-0');
    } else {
        indicator.classList.remove('opacity-0');
        indicator.classList.add('opacity-100');
    }
}
