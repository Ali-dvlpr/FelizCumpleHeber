function startGiftAnimation() {
    const giftButton = document.getElementById('gift-button');
    const animatedText = document.getElementById('animated-text');
    const giftScreen = document.getElementById('gift-screen');
    const mainContent = document.getElementById('main-content');

    const duration = 3000;
    const animationEnd = Date.now() + duration;
    const defaults = { startVelocity: 30, spread: 360, ticks: 60, zIndex: 60 };

    function randomInRange(min, max) { return Math.random() * (max - min) + min; }

    const interval = setInterval(function() {
        const timeLeft = animationEnd - Date.now();
        if (timeLeft <= 0) return clearInterval(interval);
        const particleCount = 50 * (timeLeft / duration);
        
        // Colores de plasma y tecnología
        confetti(Object.assign({}, defaults, { particleCount, origin: { x: randomInRange(0.1, 0.3), y: Math.random() - 0.2 }, colors: ['#4cc9f0', '#ffffff', '#4361ee'] }));
        confetti(Object.assign({}, defaults, { particleCount, origin: { x: randomInRange(0.7, 0.9), y: Math.random() - 0.2 }, colors: ['#1e40af', '#4cc9f0', '#f8fafc'] }));
    }, 250);

    giftButton.style.opacity = '0';
    setTimeout(() => {
        giftButton.style.display = 'none';
        animatedText.classList.remove('hidden');
        void animatedText.offsetWidth; // Reflow
        
        animatedText.classList.remove('opacity-0', 'scale-50');
        animatedText.classList.add('opacity-100', 'scale-100');

        setTimeout(() => {
            const headerText = document.getElementById('header-text');
            
            // Preparar el contenedor final
            mainContent.classList.remove('hidden');
            headerText.style.visibility = 'hidden'; // Ocultar totalmente el texto real para evitar sombras/fantasmas
            void mainContent.offsetWidth; // Reflow
            
            const startRect = animatedText.getBoundingClientRect();
            const targetRect = headerText.getBoundingClientRect();
            const scale = targetRect.width / startRect.width;
            
            // Cambiar a posicionamiento estático absoluto
            animatedText.style.transition = 'none';
            animatedText.style.top = startRect.top + 'px';
            animatedText.style.left = startRect.left + 'px';
            animatedText.style.transformOrigin = 'top left';
            animatedText.style.transform = 'translate(0px, 0px) scale(1)';
            
            animatedText.classList.remove('top-1/2', 'left-1/2', '-translate-x-1/2', '-translate-y-1/2', 'scale-100');
            
            void animatedText.offsetWidth; // Reflow
            
            // Iniciar animación hacia el objetivo
            animatedText.style.transition = 'transform 1s ease-in-out, opacity 1s ease-in-out';
            
            const translateX = targetRect.left - startRect.left;
            const translateY = targetRect.top - startRect.top;
            
            animatedText.style.transform = `translate(${translateX}px, ${translateY}px) scale(${scale})`;
            
            mainContent.classList.remove('opacity-0');
            mainContent.classList.add('opacity-100');
            
            setTimeout(() => {
                // Intercambiar suavemente
                headerText.style.opacity = '0';
                headerText.style.visibility = 'visible';
                void headerText.offsetWidth; // Reflow for transition
                headerText.style.transition = 'opacity 0.3s ease-in-out';
                headerText.style.opacity = '1';
                animatedText.style.opacity = '0';
                
                setTimeout(() => { 
                    giftScreen.style.display = 'none'; 
                    animatedText.classList.add('hidden');
                }, 300);
            }, 1000);
        }, 2500);
    }, 500);
}

